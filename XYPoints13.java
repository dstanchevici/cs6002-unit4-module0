class XYPoint {
    double x;
    double y;
}

class XYSqure {

    XYPoint corner;
    double side;

    public void printOppositeCorner ()
    {
	System.out.println ("Opposite Corner: x=" + (corner.x+side) +  " y=" + (corner.y+side));
    }
}

public class XYPoints13 {

    public static void main (String[] argv)
    {
	XYPoint p = new XYPoint ();
	p.x = 2; p.y = 3;

	XYSqure s = new XYSqure ();
	s.corner = p;
	s.side = 4;
        s.printOppositeCorner ();
    }
}
